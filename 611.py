l = []
while True:
    n = int(input("Digite um número para sair: (0 - para sair)"))
    if n == 0:
        break
    l.append(n)
for e in l:
    print(e)
print("\n")
print("Vamos gerar alguns numeros aleatórios!!!!!!!!!!!!")
for v in range(10):
    print(v)
